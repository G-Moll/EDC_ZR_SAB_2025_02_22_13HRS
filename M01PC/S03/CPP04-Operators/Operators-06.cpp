#include <iostream>
using namespace std;

int main() {
    // Ternary Operator
    // Validar una prueba y dependiendo del resultado, nos da uno de 2 posibles valores
    // ? :
    // test ? vv : vf
    
    cout << ( 1 == 1 ? "Son iguales" : "No Son iguales" ) << endl;
    cout << ( 1 == 2 ? "Son iguales" : "No Son iguales" ) << endl;
    return 0;
}
