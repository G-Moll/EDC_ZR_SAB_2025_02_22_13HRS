#include <iostream>
using namespace std;

int main() {
    // Aritmethic Operators
    // + Addition
    // - Substraction
    // * Multiplication
    // / Division
    // % Modulus
    cout << ( 5 + 10 ) << endl;
    cout << ( 5 - 10 ) << endl;
    cout << ( 5 * 10 ) << endl;
    cout << ( 5 / 10 ) << endl;
    cout << ( 5 % 10 ) << endl;
    return 0;
}
